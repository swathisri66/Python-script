#if else operators
x = -100

if x != 100:
    print("x is = ")
    print(x)
if x > 0:
    print("x is positive")
else:
    print("x is negative") 
print("finish")  

#if else with boolean operations

x = 100

if x != 100 and x < 0:
    print("x is = ")
    print(x)
if x > 0:
    print("x is positive")
else:
    print("x is negative")
